function getCurrentLanguage() {
  return localStorage.getItem(LANG_KEY) === "en" ? "en" : "ru";
}

function getCurrentLocale() {
  return getCurrentLanguage() === "en" ? "en-US" : "ru-RU";
}

function interpolateTranslation(text, values = {}) {
  return String(text).replace(/\{(\w+)\}/g, (match, key) => (
    Object.prototype.hasOwnProperty.call(values, key) ? values[key] : match
  ));
}

function getTranslation(key, values = {}) {
  const language = getCurrentLanguage();
  const text = i18Obj[language][key] ?? i18Obj.ru[key] ?? key;
  return interpolateTranslation(text, values);
}

function getTranslationBySource(source, values = {}) {
  const key = i18SourceIndex[String(source || "").replace(/\s+/g, " ").trim()];
  return key ? getTranslation(key, values) : source;
}

function registerTranslation(key, ru, en) {
  i18Obj.ru[key] = ru;
  i18Obj.en[key] = en || ru;

  if (ru) {
    i18SourceIndex[ru] = key;
  }
}

