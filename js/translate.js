function getLocalizedField(item, field) {
  if (!item) {
    return "";
  }

  if (getCurrentLanguage() === "en") {
    const englishValue = item[`${field}En`];

    if (englishValue !== undefined && englishValue !== null && englishValue !== "") {
      return englishValue;
    }
  }

  return item[field] ?? "";
}

function formatLocalizedDate(value, options = {}) {
  const date = value instanceof Date ? value : new Date(value);

  if (Number.isNaN(date.getTime())) {
    return value || "";
  }

  return new Intl.DateTimeFormat(getCurrentLocale(), options).format(date);
}

function formatLocalizedNumber(value, options = {}) {
  return new Intl.NumberFormat(getCurrentLocale(), options).format(Number(value) || 0);
}

function formatLocalizedCurrency(value) {
  return new Intl.NumberFormat(getCurrentLocale(), {
    style: "currency",
    currency: "RUB",
    maximumFractionDigits: 0
  }).format(Number(value) || 0);
}

function applyElementTranslation(element) {
  const key = element.dataset.i18n;

  if (key && i18Obj.ru[key]) {
    const translatedText = getTranslation(key);

    if (element.textContent !== translatedText) {
      element.textContent = translatedText;
    }
  }

  ["placeholder", "title", "alt", "aria-label"].forEach((attribute) => {
    const dataName = `i18n${attribute.charAt(0).toUpperCase()}${attribute.slice(1).replace("-l", "L")}`;
    const attributeKey = element.dataset[dataName];

    if (attributeKey && i18Obj.ru[attributeKey]) {
      const translatedValue = getTranslation(attributeKey);

      if (element.getAttribute(attribute) !== translatedValue) {
        element.setAttribute(attribute, translatedValue);
      }
    }
  });
}

function markDynamicTranslations(root = document) {
  const elements = root.nodeType === Node.ELEMENT_NODE
    ? [root, ...root.querySelectorAll("*")]
    : [...root.querySelectorAll("*")];

  elements.forEach((element) => {
    if (element.matches("script, style, code, pre, [data-no-translate]")) {
      return;
    }

    if (!element.dataset.i18n && element.childElementCount === 0) {
      const text = element.textContent.replace(/\s+/g, " ").trim();
      const key = i18SourceIndex[text];

      if (key) {
        element.dataset.i18n = key;
      }
    }

    ["placeholder", "title", "alt", "aria-label"].forEach((attribute) => {
      const value = element.getAttribute(attribute);
      const key = i18SourceIndex[value];
      const dataAttribute = `data-i18n-${attribute}`;

      if (key && !element.hasAttribute(dataAttribute)) {
        element.setAttribute(dataAttribute, key);
      }
    });
  });
}

function updateLanguageButtons(language) {
  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.textContent = language === "ru" ? "RU | EN" : "EN | RU";
  });

  document.querySelectorAll("[data-mobile-lang]").forEach((button) => {
    button.setAttribute("aria-pressed", button.dataset.mobileLang === language ? "true" : "false");
  });
}

function getTranslate(language = getCurrentLanguage()) {
  const nextLanguage = language === "en" ? "en" : "ru";
  i18nApplying = true;
  localStorage.setItem(LANG_KEY, nextLanguage);
  document.documentElement.lang = nextLanguage;
  markDynamicTranslations(document);

  document.querySelectorAll("[data-i18n], [data-i18n-placeholder], [data-i18n-title], [data-i18n-alt], [data-i18n-aria-label]")
    .forEach(applyElementTranslation);

  const titleKey = document.querySelector("title")?.dataset.i18n;

  if (titleKey) {
    document.title = getTranslation(titleKey);
  }

  updateLanguageButtons(nextLanguage);
  i18nApplying = false;
  window.dispatchEvent(new CustomEvent("eurasia:language-change", {
    detail: { language: nextLanguage }
  }));
}

function setLanguage(language) {
  const nextLanguage = language === "en" ? "en" : "ru";

  if (nextLanguage === getCurrentLanguage()) {
    getTranslate(nextLanguage);
    return;
  }

  localStorage.setItem(LANG_KEY, nextLanguage);
  window.location.reload();
}

function bindLanguageControls() {
  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    if (button.dataset.i18nBound) {
      return;
    }

    button.dataset.i18nBound = "true";
    button.addEventListener("click", () => {
      setLanguage(getCurrentLanguage() === "ru" ? "en" : "ru");
    });
  });

  document.querySelectorAll("[data-mobile-lang]").forEach((button) => {
    if (button.dataset.i18nBound) {
      return;
    }

    button.dataset.i18nBound = "true";
    button.addEventListener("click", () => setLanguage(button.dataset.mobileLang));
  });
}

function startTranslationObserver() {
  if (i18nObserver) {
    return;
  }

  i18nObserver = new MutationObserver((mutations) => {
    if (i18nApplying) {
      return;
    }

    i18nApplying = true;
    mutations.forEach((mutation) => {
      mutation.addedNodes.forEach((node) => {
        if (node.nodeType === Node.TEXT_NODE) {
          const parent = node.parentElement;
          const source = node.nodeValue.replace(/\s+/g, " ").trim();
          const key = i18SourceIndex[source];

          if (parent && key && parent.childElementCount === 0) {
            parent.dataset.i18n = key;
            applyElementTranslation(parent);
          }
          return;
        }

        if (node.nodeType !== Node.ELEMENT_NODE) {
          return;
        }

        markDynamicTranslations(node);
        node.querySelectorAll("[data-i18n], [data-i18n-placeholder], [data-i18n-title], [data-i18n-alt], [data-i18n-aria-label]")
          .forEach(applyElementTranslation);

        if (node.matches("[data-i18n], [data-i18n-placeholder], [data-i18n-title], [data-i18n-alt], [data-i18n-aria-label]")) {
          applyElementTranslation(node);
        }
      });
    });
    bindLanguageControls();
    i18nApplying = false;
  });

  i18nObserver.observe(document.body, { childList: true, subtree: true });
}

